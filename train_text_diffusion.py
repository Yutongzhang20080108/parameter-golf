class RMSNorm(nn.Module):
    def __init__(self, eps: float | None = None):
        super().__init__()
        self.eps = eps

    def forward(self, x: Tensor) -> Tensor:
        return F.rms_norm(x, (x.size(-1),), eps=self.eps)


class CastedLinear(nn.Linear):
    # Keep weights in fp32 for optimizer/state quality, cast at matmul time for bf16 compute.
    def forward(self, x: Tensor) -> Tensor:
        bias = self.bias.to(x.dtype) if self.bias is not None else None
        return F.linear(x, self.weight.to(x.dtype), bias)


def restore_low_dim_params_to_fp32(module: nn.Module) -> None:
    # Keep small/control parameters in fp32 even when the model body runs in bf16.
    with torch.no_grad():
        for name, param in module.named_parameters():
            if (param.ndim < 2 or any(pattern in name for pattern in CONTROL_TENSOR_NAME_PATTERNS)) and param.dtype != torch.float32:
                param.data = param.data.float()


class Rotary(nn.Module):
    # Caches cos/sin tables per sequence length on the current device.
    def __init__(self, dim: int, base: float = 10000.0):
        super().__init__()
        inv_freq = 1.0 / (base ** (torch.arange(0, dim, 2, dtype=torch.float32) / dim))
        self.register_buffer("inv_freq", inv_freq, persistent=False)
        self._seq_len_cached = 0
        self._cos_cached: Tensor | None = None
        self._sin_cached: Tensor | None = None

    def forward(self, seq_len: int, device: torch.device, dtype: torch.dtype) -> tuple[Tensor, Tensor]:
        if (
            self._cos_cached is None
            or self._sin_cached is None
            or self._seq_len_cached != seq_len
            or self._cos_cached.device != device
        ):
            t = torch.arange(seq_len, device=device, dtype=self.inv_freq.dtype)
            freqs = torch.outer(t, self.inv_freq.to(device))
            self._cos_cached = freqs.cos()[None, None, :, :]
            self._sin_cached = freqs.sin()[None, None, :, :]
            self._seq_len_cached = seq_len
        return self._cos_cached.to(dtype=dtype), self._sin_cached.to(dtype=dtype)


def apply_rotary_emb(x: Tensor, cos: Tensor, sin: Tensor) -> Tensor:
    half = x.size(-1) // 2
    x1, x2 = x[..., :half], x[..., half:]
    return torch.cat((x1 * cos + x2 * sin, x1 * (-sin) + x2 * cos), dim=-1)


class TimestepEmbedding(nn.Module):
    """Maps a scalar timestep t ∈ [0, 1] to a model_dim vector.

    Pipeline:
        t  →  sinusoidal features (dim/2 sin + dim/2 cos)
           →  Linear(dim, 4*dim) + SiLU
           →  Linear(4*dim, dim)

    The resulting vector is added to the token hidden states once at the start
    of the transformer so every block is aware of the current noise level.
    """

    def __init__(self, dim: int):
        super().__init__()
        # Half the frequencies are sin, half are cos, giving `dim` features total.
        assert dim % 2 == 0, "model_dim must be even for sinusoidal timestep embedding"
        self.half_dim = dim // 2
        # Geometric sequence of frequencies, same convention as positional encoding.
        freqs = torch.exp(-math.log(10000) * torch.arange(self.half_dim, dtype=torch.float32) / self.half_dim)
        self.register_buffer("freqs", freqs, persistent=False)
        self.mlp = nn.Sequential(
            nn.Linear(dim, 4 * dim),
            nn.SiLU(),
            nn.Linear(4 * dim, dim),
        )

    def forward(self, t: Tensor) -> Tensor:
        # t: (B,) float in [0, 1]
        args = t[:, None].float() * self.freqs[None, :]  # (B, half_dim)
        emb = torch.cat([args.sin(), args.cos()], dim=-1)  # (B, dim)
        return self.mlp(emb)                               # (B, dim)


class BidirectionalSelfAttention(nn.Module):
    """Full (non-causal) self-attention.

    Identical to the GPT causal variant except `is_causal=False`: every position
    can attend to every other position, which is required for a denoising model
    that must reason about both past and future context when reconstructing masked tokens.
    """

    def __init__(
        self,
        dim: int,
        num_heads: int,
        num_kv_heads: int,
        rope_base: float,
        qk_gain_init: float,
    ):
        super().__init__()
        if dim % num_heads != 0:
            raise ValueError("model_dim must be divisible by num_heads")
        if num_heads % num_kv_heads != 0:
            raise ValueError("num_heads must be divisible by num_kv_heads")
        self.num_heads = num_heads
        self.num_kv_heads = num_kv_heads
        self.head_dim = dim // num_heads
        if self.head_dim % 2 != 0:
            raise ValueError("head_dim must be even for RoPE")
        kv_dim = self.num_kv_heads * self.head_dim
        self.c_q = CastedLinear(dim, dim, bias=False)
        self.c_k = CastedLinear(dim, kv_dim, bias=False)
        self.c_v = CastedLinear(dim, kv_dim, bias=False)
        self.proj = CastedLinear(dim, dim, bias=False)
        self.proj._zero_init = True
        self.q_gain = nn.Parameter(torch.full((num_heads,), qk_gain_init, dtype=torch.float32))
        self.rotary = Rotary(self.head_dim, base=rope_base)

    def forward(self, x: Tensor) -> Tensor:
        bsz, seqlen, dim = x.shape
        q = self.c_q(x).reshape(bsz, seqlen, self.num_heads, self.head_dim).transpose(1, 2)
        k = self.c_k(x).reshape(bsz, seqlen, self.num_kv_heads, self.head_dim).transpose(1, 2)
        v = self.c_v(x).reshape(bsz, seqlen, self.num_kv_heads, self.head_dim).transpose(1, 2)
        q = F.rms_norm(q, (q.size(-1),))
        k = F.rms_norm(k, (k.size(-1),))
        cos, sin = self.rotary(seqlen, x.device, q.dtype)
        q = apply_rotary_emb(q, cos, sin)
        k = apply_rotary_emb(k, cos, sin)
        q = q * self.q_gain.to(dtype=q.dtype)[None, :, None, None]
        # is_causal=False: diffusion denoisers need full bidirectional context.
        y = F.scaled_dot_product_attention(
            q,
            k,
            v,
            attn_mask=None,
            is_causal=False,
            enable_gqa=(self.num_kv_heads != self.num_heads),
        )
        y = y.transpose(1, 2).contiguous().reshape(bsz, seqlen, dim)
        return self.proj(y)


class MLP(nn.Module):
    # relu^2 MLP from the original modded-nanogpt setup
    def __init__(self, dim: int, mlp_mult: int):
        super().__init__()
        hidden = mlp_mult * dim
        self.fc = CastedLinear(dim, hidden, bias=False)
        self.proj = CastedLinear(hidden, dim, bias=False)
        self.proj._zero_init = True

    def forward(self, x: Tensor) -> Tensor:
        x = torch.relu(self.fc(x))
        return self.proj(x.square())


class Block(nn.Module):
    def __init__(
        self,
        dim: int,
        num_heads: int,
        num_kv_heads: int,
        mlp_mult: int,
        rope_base: float,
        qk_gain_init: float,
    ):
        super().__init__()
        self.attn_norm = RMSNorm()
        self.mlp_norm = RMSNorm()
        self.attn = BidirectionalSelfAttention(dim, num_heads, num_kv_heads, rope_base, qk_gain_init)
        self.mlp = MLP(dim, mlp_mult)
        self.attn_scale = nn.Parameter(torch.ones(dim, dtype=torch.float32))
        self.mlp_scale = nn.Parameter(torch.ones(dim, dtype=torch.float32))
        self.resid_mix = nn.Parameter(torch.stack((torch.ones(dim), torch.zeros(dim))).float())

    def forward(self, x: Tensor, x0: Tensor) -> Tensor:
        mix = self.resid_mix.to(dtype=x.dtype)
        x = mix[0][None, None, :] * x + mix[1][None, None, :] * x0
        attn_out = self.attn(self.attn_norm(x))
        x = x + self.attn_scale.to(dtype=x.dtype)[None, None, :] * attn_out
        x = x + self.mlp_scale.to(dtype=x.dtype)[None, None, :] * self.mlp(self.mlp_norm(x))
        return x


class TextDiffusionModel(nn.Module):
    """Masked absorbing diffusion model for discrete text.

    The model learns to denoise: given a partially-masked token sequence x_t and
    the noise level t, predict the original clean token at every masked position.

    Token vocabulary layout:
        ids 0 … vocab_size-1  →  real tokens
        id  vocab_size         →  [MASK] token (used only during corruption)

    The output head predicts over the real vocabulary only (vocab_size logits).
    """

    def __init__(
        self,
        vocab_size: int,
        num_layers: int,
        model_dim: int,
        num_heads: int,
        num_kv_heads: int,
        mlp_mult: int,
        rope_base: float,
        qk_gain_init: float,
        num_diffusion_steps: int,
    ):
        super().__init__()
        self.vocab_size = vocab_size
        self.mask_token_id = vocab_size          # MASK lives just above the real vocab
        self.num_diffusion_steps = num_diffusion_steps

        # +1 in the embedding table to accommodate the MASK token.
        self.tok_emb = nn.Embedding(vocab_size + 1, model_dim)
        self.time_emb = TimestepEmbedding(model_dim)

        # U-Net style skip connections: encoder (first half) outputs are added back
        # in reverse order to the decoder (second half) inputs.
        self.num_encoder_layers = num_layers // 2
        self.num_decoder_layers = num_layers - self.num_encoder_layers
        self.num_skip_weights = min(self.num_encoder_layers, self.num_decoder_layers)
        self.skip_weights = nn.Parameter(torch.ones(self.num_skip_weights, model_dim, dtype=torch.float32))

        self.blocks = nn.ModuleList(
            [
                Block(model_dim, num_heads, num_kv_heads, mlp_mult, rope_base, qk_gain_init)
                for _ in range(num_layers)
            ]
        )
        self.final_norm = RMSNorm()
        # Output head projects to the real vocabulary only (MASK is never a prediction target).
        self.lm_head = CastedLinear(model_dim, vocab_size, bias=False)
        self.lm_head._zero_init = True
        self._init_weights()

    def _init_weights(self) -> None:
        for module in self.modules():
            if isinstance(module, nn.Linear) and getattr(module, "_zero_init", False):
                nn.init.zeros_(module.weight)

    def _run_transformer(self, x_t_ids: Tensor, t: Tensor) -> Tensor:
        """Shared encoder-decoder pass used by both forward() and denoise().

        Args:
            x_t_ids: corrupted token ids, shape (B, T).
            t: continuous timestep per sequence, shape (B,), values in (0, 1].

        Returns:
            logits over the real vocabulary, shape (B, T, vocab_size).
        """
        # Embed corrupted tokens and add timestep conditioning.
        x = self.tok_emb(x_t_ids)                            # (B, T, D)
        x = F.rms_norm(x, (x.size(-1),))
        x = x + self.time_emb(t.to(x.device))[:, None, :]   # broadcast (B, 1, D)
        x0 = x  # anchor for residual mix inside each Block

        # Encoder: first half of blocks; save outputs for U-Net skip connections.
        skips: list[Tensor] = []
        for i in range(self.num_encoder_layers):
            x = self.blocks[i](x, x0)
            skips.append(x)

        # Decoder: second half of blocks; consume saved encoder outputs in reverse.
        for i in range(self.num_decoder_layers):
            if skips:
                x = x + self.skip_weights[i].to(dtype=x.dtype)[None, None, :] * skips.pop()
            x = self.blocks[self.num_encoder_layers + i](x, x0)

        return self.lm_head(self.final_norm(x))               # (B, T, vocab_size)

    def forward(self, x_0: Tensor) -> Tensor:
        """Training step: corrupt x_0 with a random timestep, then predict x_0.

        Args:
            x_0: clean token ids, shape (B, T).

        Returns:
            Scalar diffusion loss (mean CE over masked positions).
        """
        B = x_0.size(0)

        # Sample a random discrete timestep k ∈ {1, …, T} for each sequence,
        # then convert to continuous t = k / T ∈ (0, 1].
        k = torch.randint(1, self.num_diffusion_steps + 1, (B,), device=x_0.device)
        t = k.float() / self.num_diffusion_steps              # (B,), values in (0, 1]

        # Corrupt: mask each token independently with probability 1 - alpha_bar(t).
        alpha_bar = cosine_alpha_bar(t)                        # (B,)
        x_t = corrupt_tokens(x_0, alpha_bar, self.mask_token_id)

        # Predict clean tokens from corrupted input.
        logits = self._run_transformer(x_t, t)                # (B, T, vocab_size)

        # Loss only at masked positions (unmasked positions are trivially correct).
        # We apply the mask via ignore_index so torch.compile sees a static graph:
        # positions that were NOT masked get target = -1, which cross_entropy skips.
        targets = torch.where(x_t == self.mask_token_id, x_0, torch.full_like(x_0, -1))
        return F.cross_entropy(
            logits.reshape(-1, self.vocab_size).float(),
            targets.reshape(-1),
            ignore_index=-1,
            reduction="mean",
        )

    @torch.no_grad()
    def denoise(self, x_t: Tensor, t: float) -> Tensor:
        """Single denoising step used at inference: returns predicted clean token ids.

        Args:
            x_t: partially-masked token ids, shape (B, T).
            t: scalar timestep in (0, 1].

        Returns:
            Predicted clean token ids (argmax), shape (B, T).
        """
        t_batch = torch.full((x_t.size(0),), t, dtype=torch.float32, device=x_t.device)
        logits = self._run_transformer(x_t, t_batch)          # (B, T, vocab_size)
        return logits.argmax(dim=-1)


