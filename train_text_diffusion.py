"""
Text Diffusion Language Model (Masked Absorbing Diffusion)

Architecture overview:
  Forward process q:  x_0 (clean tokens) → x_t (tokens randomly masked based on timestep t)
  Reverse process p:  model(x_t, t) predicts the original clean token at each masked position
  Training loss:      cross-entropy at masked positions, averaged over t ~ Uniform(0, 1]

Key differences from the GPT baseline:
  - Attention is bidirectional (no causal mask) so each position can see all others
  - A special MASK token (id = vocab_size) is added to the embedding table
  - A TimestepEmbedding projects the scalar t ∈ [0,1] to model_dim and is added to
    the token hidden states so every block is aware of the noise level
  - The model receives a fully-corrupted x_t and must recover x_0, not predict x_{i+1}
"""

